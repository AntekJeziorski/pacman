    public MazeGenerator() {
        walls = new ArrayList<>();
        dots = new ArrayList<>();
        for(int i = 0; i < map.length; i++)
        {
            for(int j = 0; j < map[0].length; j++)
            {
                switch (map[i][j]) {
                    case 0 -> { // Air
                        walls.add(new Air(j, i));
                        dots.add(new Air(j, i));
                    }
                    case 1 -> { // Wall
                        walls.add(new Wall(j, i));
                        dots.add(new Air(j, i));
                    }
                    case 2 -> { // Dot
                        walls.add(new Air(j, i));
                        dots.add(new Dot(j, i));
                    }
                    case 3 -> { // Apple
                        walls.add(new Air(j, i));
                        dots.add(new Apple(j, i));
                    }
                    default -> {
                    }
                }
            }
        }
    }

    public void showMaze(Graphics graph, MazePanel maze) {
        for(SceneObject wall: walls) {
            wall.show(graph, maze);
        }
        for(SceneObject dot: dots) {
            dot.show(graph, maze);
        }
    }

    public List<SceneObject> getWalls() {
        return walls;
    }

    public List<SceneObject> getDots() { return dots; }

    public void deleteDot(int pos, int x, int y) {
        dots.set(pos, new Air(x, y));
    }

